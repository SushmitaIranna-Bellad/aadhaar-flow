// Helper to get unique values
export const getUniqueStates = () => [...new Set(aadhaarCenters.map(c => c.state))].sort();
export const getUniqueDistricts = (state?: string) => {
  const centers = state ? aadhaarCenters.filter(c => c.state === state) : aadhaarCenters;
  return [...new Set(centers.map(c => c.district))].sort();
};
export const getUniqueTaluks = (district?: string) => {
  const centers = district ? aadhaarCenters.filter(c => c.district === district) : aadhaarCenters;
  return [...new Set(centers.map(c => c.taluk))].sort();
};
